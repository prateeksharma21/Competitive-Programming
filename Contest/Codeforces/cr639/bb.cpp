void test(){
   int n;
   cin >> n;
   int ans = 0;
   int sq = sqrt(n)+1;
   for(int i=sq;i>=1;i--){
      int sm = (i*(i-1)/2)*3+2*i;
      if(sm <= n){
         int v = n/sm;
         n -= v*sm;
         ans+=v;
      }
   }
   cout << ans << '\n';
   return;
}

